public class Assignment_operator {
    public static void main(String args[]){
        int a = 10; // This = here is known as basic assignment operator and will definitely used most in the program
        a+=1;
        a++;
        int result = ++a;
        System.out.println(result);


    }
}


// There are some compound assignment opertors are there they are [+=,-=,/=,*=,++,-- (these work in the pre order and post order)]